// Create a function that returns true if an integer is evenly divisible by 5, and false otherwise.

function divisibleByFive(n) {
	
if (n % 5 === 0) {
   console.log(true)
} else {
  console.log(false)
}


}





divisibleByFive(5) // ➞ true

divisibleByFive(-55) // ➞ true

divisibleByFive(37) // ➞ false